package com.progra.guia.logic;

/**
 *
 * @author joas2
 */
public class Marca {
    private String id;
    private String nombre;

    public Marca(String id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Marca() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Marca other = (Marca) obj;
        return this.id == other.id;
    }
    
    
}
