package com.progra.guia.logic;

/**
 *
 * @author PC
 */
public class Modelo {
      private String id;
    private String nombre;

    public Modelo(String id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Modelo() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Modelo other = (Modelo) obj;
        return this.id == other.id;
    }
}
